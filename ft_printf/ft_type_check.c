#include "ft_printf.h"

int	ft_type_check(const char a, const char b)
{
	if (a == '%' && (b == 'c' || b == 's' || b == 'p' || b == 'd' || b == 'i'
			|| b == 'u' || b == 'x' || b == 'X' || b == '%'))
		return (1);
	else
		return (0);
}
